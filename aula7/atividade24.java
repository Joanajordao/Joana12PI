public class atividade24 {
    public static void main(String[] args) {
        //matriz 3x2
        int matriz [][] = {{1, 2, 3},{4, 5, 6}};

        for (int i = 0; i < matriz.length; i++){
            for (int x = 0; x < matriz[i].length; x++){
                System.out.printf(matriz[i][x] + " ");
            }
            System.out.println();
        }
    }
}

